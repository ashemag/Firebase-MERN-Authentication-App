import {
  USER_LOGIN_REQUEST,
  USER_NOT_CONFIRMED,
  USER_LOGIN_SUCCESS,
  USER_LOGIN_FAIL,
  USER_LOGOUT,
  USER_REGISTER_REQUEST,
  USER_REGISTER_SUCCESS,
  USER_REGISTER_FAIL,
  USER_LOGIN_EMAIL_VERIFICATION_SUCCESS,
  USER_LOGIN_EMAIL_PASSWORD_RESET_SUCCESS,
} from "../constants/userConstants";

export const userLoginReducer = (state = {}, action) => {
  switch (action.type) {
    case USER_LOGIN_REQUEST:
      return { loading: true };
    case USER_NOT_CONFIRMED:
      return { loading: false, error: action.payload };
    case USER_LOGIN_SUCCESS:
      return { loading: false, userInfo: action.payload };
    case USER_LOGIN_FAIL:
      return { loading: false, error: action.payload };
    case USER_LOGIN_EMAIL_VERIFICATION_SUCCESS:
      return { loading: false, emailVerificationSent: true };
    case USER_LOGIN_EMAIL_PASSWORD_RESET_SUCCESS:
      return { loading: false, emailPasswordResetSent: true };
    case USER_LOGOUT:
      return {};
    default:
      return state;
  }
};

export const userRegisterReducer = (state = {}, action) => {
  switch (action.type) {
    case USER_REGISTER_REQUEST:
      return { loading: true };
    case USER_REGISTER_SUCCESS:
      return { loading: false, userInfo: action.payload };
    case USER_REGISTER_FAIL:
      return { loading: false, error: action.payload };
    case USER_LOGOUT:
      return {};
    default:
      return state;
  }
};
